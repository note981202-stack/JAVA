package Lamda;

public class Student {

	private int id;
	private int age;
	private double gpa;
	
	public Student(int id, int age, double gpa) {
		this.id = id;
		this.age = age;
		this.gpa = gpa;
	}

	public int getId() {
		return id;
	}

	public int getAge() {
		return age;
	}

	public double getGpa() {
		return gpa;
	}
	
	}

